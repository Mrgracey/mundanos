<h3 class="has-text-align-center"><strong>¿QUÉ?</strong></h3>



<p style="font-size:17px;text-align:center;" class="has-text-color has-background has-dark-gray-color has-white-background-color">MUNDANOS
 habla de nosotros, los comunes: de lo que somos, lo que hacemos, lo que
 queremos ser. Retrata fragmentos de vidas que, como retazos, componen 
un todo mayor que es el mundo.<br>MUNDANOS pretende ser la voz de todos y
 de nadie a la vez; porque no pertenece, pero no se borra ni se calla: 
registra los pasos imperceptibles, las caras desconocidas, las vidas de 
aquellos que construimos -desde abajo- la historia de la humanidad.<br><br>Si querés saber más sobre la propuesta concreta, hace <a href="#" onClick="cargarPropuesta()">click acá</a>.</p>



<h3 class="has-text-align-center"><strong>¿CÓMO?</strong> </h3>



<p style="font-size:17px;text-align:center;" class="has-text-color has-dark-gray-color">
 La propuesta concreta de MUNDANOS nace de unir la humanidad a través de
 diferentes lenguajes: la fotografía; la literatura, la poesía y las 
palabras; la filosofía; los viajes y la historia. Refleja fragmentos de 
vidas en distintas formas y de lugares distantes del mundo para hablar 
de nuestra diversidad, de nuestras contradicciones, de lo que nos hace 
humanos, de los pueblos del mundo que a veces, sin saberlo, nos 
parecemos tanto. Así, vas a encontrar en MUNDANOS  fotos de la 
cotidianidad (la mundanidad) de la gente en otros países, testimonios de
 voces, entrevistas, relatos viajeros, historia y contexto, 
recomendaciones, y muchas cosas más. <br><br>Elegí por qué país empezar haciendo <a href="#" onClick="cargarIndexPais()">click acá</a></p>



<h3 class="has-text-align-center"><strong>¿QUIÉN SOY?</strong></h3>



<p style="font-size:17px;text-align:center;" class="has-text-color has-dark-gray-color">Mi nombre es
Lucía Gracey. Vengo de Argentina y de América Latina, estudié Historia en la
universidad, escribo desde que tengo memoria, viajo intermitentemente hace cinco
años y amo la fotografía. Hice este proyecto para condensar todas mis pasiones
y por la necesidad de mostrar el mundo para reconstruirlo, para encontrarnos,
para mirarnos a nosotros y entender nuestra diversidad como algo que nos
enriquece y no que nos separa. &nbsp;</p>



